    private void settingSaveBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_settingSaveBtActionPerformed
        // TODO add your handling code here:
        if(this.ss.getSetting().getSettingId() == null){
            this.chargeSetting();
            if(this.ss.saveSetting()){
                GeneralParameter.chargeSetting();
                JOptionPane.showMessageDialog(this, "Configuración guardada correctamente");
            }
        }else if(this.ss.getSetting().getSettingId() != null){
            this.chargeSetting();
            if(this.ss.updateSetting()){
                GeneralParameter.chargeSetting();
                JOptionPane.showMessageDialog(this, "Configuración actualizada correctamente");
            }
        }
        
        
    }//GEN-LAST:event_settingSaveBtActionPerformed
    
    private void chargeSetting(){
        this.ss.getSetting().setIVA_VALUE(Double.valueOf(settingIvaValueTxt.getText()));
        this.ss.getSetting().setACCURACY_VALUE(Integer.valueOf(String.valueOf(settingAccuracyValueSp.getValue())));
        this.ss.getSetting().setAUTHORIZATION_NUMBER(settingAuthorizationNumberTxt.getText());
        this.ss.getSetting().setFIRST_INVOICE_NUMBER(settingFirstNumberTxt.getText());
        this.ss.getSetting().setSECOND_INVOICE_NUMBER(settingSecondNumberTxt.getText());
        this.ss.getSetting().setTHIRD_INVOICE_NUMBER(settingThirdNumberTxt.getText());
        this.ss.getSetting().setCANVAS_PRINT_VALUE(Double.valueOf(settingCanvasPrintPriceTxt.getText()));
        this.ss.getSetting().setVYNIL_PRINT_VALUE(Double.valueOf(settingVynilPrintPriceTxt.getText()));
        if(settingPrintPreview.isSelected()){
            this.ss.getSetting().setSHOW_PRINT_PREVIEW(Boolean.TRUE);
        }else{
            this.ss.getSetting().setSHOW_PRINT_PREVIEW(Boolean.FALSE);
        }
        this.ss.getSetting().setSIMPLE_SERVICE_VALUE(Integer.parseInt(String.valueOf(simpleServiceValueSp.getValue())));
        this.ss.getSetting().setMEDIUM_SERVICE_VALUE(Integer.parseInt(String.valueOf(normalServiceValueSp.getValue())));
        this.ss.getSetting().setCOMPLEX_SERVICE_VALUE(Integer.parseInt(String.valueOf(complexServiceValueSp.getValue())));
        this.ss.getSetting().setPUBLICIST_PRINT_DISCOUNT_VALUE(Integer.parseInt(String.valueOf(printDiscountSp.getValue())));
        this.ss.getSetting().setPUBLICIST_FINISHED_DISCOUNT_VALUE(Integer.parseInt(String.valueOf(finishedDiscountSp.getValue())));
        this.ss.getSetting().setPUBLICIST_SERVICE_DISCOUNT_VALUE(Integer.parseInt(String.valueOf(serviceDiscountSp.getValue())));
    }
    
    private void chargeViewSetting(){
        settingIvaValueTxt.setText(String.valueOf(this.ss.getSetting().getIVA_VALUE().intValue()));
        settingAccuracyValueSp.setValue(this.ss.getSetting().getACCURACY_VALUE());
        settingAuthorizationNumberTxt.setText(this.ss.getSetting().getAUTHORIZATION_NUMBER());
        settingFirstNumberTxt.setText(this.ss.getSetting().getFIRST_INVOICE_NUMBER());
        settingSecondNumberTxt.setText(this.ss.getSetting().getSECOND_INVOICE_NUMBER());
        settingThirdNumberTxt.setText(this.ss.getSetting().getTHIRD_INVOICE_NUMBER());
        settingCanvasPrintPriceTxt.setText(String.valueOf(this.ss.getSetting().getCANVAS_PRINT_VALUE()));
        settingVynilPrintPriceTxt.setText(String.valueOf(this.ss.getSetting().getVYNIL_PRINT_VALUE()));
        if(this.ss.getSetting().getSHOW_PRINT_PREVIEW()){
            settingPrintPreview.setSelected(true);
        }
        simpleServiceValueSp.setValue(this.ss.getSetting().getSIMPLE_SERVICE_VALUE());
        normalServiceValueSp.setValue(this.ss.getSetting().getMEDIUM_SERVICE_VALUE());
        complexServiceValueSp.setValue(this.ss.getSetting().getCOMPLEX_SERVICE_VALUE());
        printDiscountSp.setValue(this.ss.getSetting().getPUBLICIST_PRINT_DISCOUNT_VALUE());
        finishedDiscountSp.setValue(this.ss.getSetting().getPUBLICIST_FINISHED_DISCOUNT_VALUE());
        serviceDiscountSp.setValue(this.ss.getSetting().getPUBLICIST_SERVICE_DISCOUNT_VALUE());
    }


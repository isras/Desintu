    public void signOutMessage() {
        int valor = JOptionPane.showConfirmDialog(this, "Esta seguro de cerrar la sesión?", "Advertencia", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if (valor == JOptionPane.YES_OPTION) {
            cleanSignOutViews();
            disableMainViewButtons();
        }
    }

    public void enableMainViewButtons() {
        this.homeButton.setEnabled(true);
        this.petitionPJB.setEnabled(true);
        this.clientsPrincipalButton.setEnabled(true);
        this.productPJB.setEnabled(true);
        this.facturePJB.setEnabled(true);
        this.cashSplitB.setEnabled(true);
        this.preferenceButton.setEnabled(true);
    }

    public void disableMainViewButtons() {
        this.homeButton.setEnabled(false);
        this.petitionPJB.setEnabled(false);
        this.clientsPrincipalButton.setEnabled(false);
        this.productPJB.setEnabled(false);
        this.facturePJB.setEnabled(false);
        this.cashSplitB.setEnabled(false);
        this.preferenceButton.setEnabled(false);
    }

    public void cleanSignOutViews() {
        this.userLogInTxt.setText("");
        this.passwordLogInTxt.setText("");
        this.welcomeUserLabel.setText("");
        Sessions.setUserAccount(null);
    }

    private void selectionControl(Boolean b, JButton button) {
        for (int i = 0; i < ButtonPanelBG.getButtonCount(); i++) {
            if (Operaciones.getSelectionButton(ButtonPanelBG).equals(button)) {
                button.setSelected(true);
            }
        }
    }


    private void addItemsToAccountPopup() {

        if (Sessions.getUser() != null) {
            if (Sessions.getUser().getRol().getRolName().equals("Administrador")) {
                myAccountItem.setEnabled(true);
                accountOptionsPopup.add(myAccountItem);
                userAdminItem.setEnabled(true);
                accountOptionsPopup.add(userAdminItem);
                preferencesItem.setEnabled(true);
                accountOptionsPopup.add(preferencesItem);
                accountOptionsPopup.add(jSeparator1);
                logInAccountItem.setText("Cerrar sesión");
                accountOptionsPopup.add(logInAccountItem);
            } else {
                myAccountItem.setEnabled(true);
                accountOptionsPopup.add(myAccountItem);
                userAdminItem.setEnabled(false);
                accountOptionsPopup.add(userAdminItem);
                preferencesItem.setEnabled(true);
                accountOptionsPopup.add(preferencesItem);
                accountOptionsPopup.add(jSeparator1);
                logInAccountItem.setText("Cerrar sesión");
                accountOptionsPopup.add(logInAccountItem);
            }

        } else {
            myAccountItem.setEnabled(false);
            accountOptionsPopup.add(myAccountItem);
            userAdminItem.setEnabled(false);
            accountOptionsPopup.add(userAdminItem);
            preferencesItem.setEnabled(false);
            accountOptionsPopup.add(preferencesItem);
            accountOptionsPopup.add(jSeparator1);
            logInAccountItem.setText("Iniciar Sesión");
            accountOptionsPopup.add(logInAccountItem);
        }
    }


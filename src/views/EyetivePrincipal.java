    private void signIn(){
        UserAccountService us = new UserAccountService();
        Sessions.setUserAccount(us.logIn(userLogInTxt.getText(), passwordLogInTxt.getText()));
        if (Sessions.getUser() != null) {
            //this.dispose();
            enableMainViewButtons();
            new Explode(contentPanel, new HomePanel("/images/backgrounds/homeArmoire.png")).play();
            welcomeUserLabel.setText("Bienvenido, " + Sessions.getUser().getEmployee().toString());
            GeneralParameter.chargeSetting();
        } else {
            JOptionPane.showMessageDialog(this, "No esta registrado en el sistema o su usuario o clave son incorrectos");
        }
    }
    

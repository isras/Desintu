    private void totalCalculate() {
        billTotal = oDollars + fDollars + tDollars + twDollars + ftDollars + ohDollars;
        billTotalTxt.setText(String.valueOf(Operaciones.parteDecimal(Math.rint(billTotal*auxTot)/auxTot, GeneralParameter.ACCURACY_VALUE)));
        
        coinTotal = oCent + fCents + tCents + tfCents + ftCents + ohCents;
        
        coinTotalTxt.setText(String.valueOf(Operaciones.parteDecimal(Math.rint(coinTotal*auxTot)/auxTot, GeneralParameter.ACCURACY_VALUE)));
        cashierCashtxt.setText(String.valueOf(Operaciones.parteDecimal(Math.rint((billTotal + coinTotal)*auxTot)/auxTot, GeneralParameter.ACCURACY_VALUE)));
        cashierCash = Double.valueOf(cashierCashtxt.getText());
        
        cashierInflowTotal = cashierCash + cashierTotalCheks + cashierTotalCreditCard + cashierTotalDebitCard;
        
        cashierInflowTotalTxt.setText(String.valueOf(Operaciones.parteDecimal(Math.rint(cashierInflowTotal*auxTot)/auxTot, GeneralParameter.ACCURACY_VALUE)));
        reicivableTotal = Double.valueOf(reicivableTotalTxt.getText());
        sysCashInit = Double.valueOf(sysCashInittxt.getText());
        
        sysInflowTotal = sysCashInit + sysCashTotal + sysTotalCheks + sysTotalCreditCard + sysTotalDebitCard;
        
        sysInflowTotalTxt.setText(String.valueOf(Operaciones.parteDecimal(Math.rint(sysInflowTotal*auxTot)/auxTot, GeneralParameter.ACCURACY_VALUE)));
        
        cashTotal = sysInflowTotal - outflowTotal;
        
        cashTotalTxt.setText(String.valueOf(Operaciones.parteDecimal(Math.rint(cashTotal*auxTot)/auxTot, GeneralParameter.ACCURACY_VALUE)));
    }

    private void chargeClosingCashData() {
        this.ccs.getCashClosing().setCcDateFrom(cCashDateFrom.getDate());
        this.ccs.getCashClosing().setCcDateTo(cCashDateTo.getDate());
        this.ccs.getCashClosing().setCcInitialCashValue(sysCashInit);
        this.ccs.getCashClosing().setCcTotalCash(sysCashTotal);
        this.ccs.getCashClosing().setCcTotalCheks(sysTotalCheks);
        this.ccs.getCashClosing().setCcTotalCreditCard(sysTotalCreditCard);
        this.ccs.getCashClosing().setCcTotalDebitCard(sysTotalDebitCard);
        this.ccs.getCashClosing().setCcTotalInflow(sysInflowTotal);
        this.ccs.getCashClosing().setCcTotalOutflow(outflowTotal);
        this.ccs.getCashClosing().setCcTotalReceivableAccounts(reicivableTotal);
        this.ccs.getCashClosing().setCcTotal(cashTotal);
        this.ccs.getCashClosing().setCcObservation(closingCashObservations.getText());
    }


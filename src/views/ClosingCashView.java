    private void outflowTotalCalculate() {
        //fijamos a la lista de asientos contables el resultado de los registros de una fecha espec√≠fica
        this.aes.setAccountingEntryList(this.aes.listByCurrentDate(new Date()));
        //Si no tiene datos de ingresos y egresos muestra un mensaje
        //If don¬¥t have data of inflows and outflows show a message
        if (this.aes.getAccountingEntryList().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No existen valores para el cierre de caja");
        } else {
            //M√©todo para calcular el total de los ingresos y egresos
            for (int i = 0; i < this.aes.getAccountingEntryList().size(); i++) {
                if (this.aes.getAccountingEntryList().get(i).getAeType().equals(0)) {
                    sysCashTotal += this.aes.getAccountingEntryList().get(i).getAeValue();
                } else {
                    outflowTotal += this.aes.getAccountingEntryList().get(i).getAeValue();
                }
            }
            sysCashInittxt.setText(String.valueOf(Operaciones.parteDecimal(Math.rint(this.aes.getAccountingEntryList().get(0).getAeValue()*auxTot)/auxTot,GeneralParameter.ACCURACY_VALUE)));
            sysCashTotal -= this.aes.getAccountingEntryList().get(0).getAeValue();
            sysCashtxt.setText(String.valueOf(Operaciones.parteDecimal(Math.rint(sysCashTotal*auxTot)/auxTot, GeneralParameter.ACCURACY_VALUE)));
            outflowTotalTxt.setText(String.valueOf(Operaciones.parteDecimal(Math.rint(outflowTotal*auxTot)/auxTot, GeneralParameter.ACCURACY_VALUE)));
            //Calculamos los totales, ingresos, egresos y cuentas por cobrar y fijamos la los valores en la vista.
            this.totalCalculate();
        }
    }

